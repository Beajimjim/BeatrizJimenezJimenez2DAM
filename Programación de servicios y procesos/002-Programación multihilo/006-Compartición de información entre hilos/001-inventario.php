<?php
    // Declaramos una variable llamada $inventario y la inicializamos con el valor 10.
    $inventario = 10;

    // Disminuimos en 1 el valor de $inventario usando el operador de decremento (--).
    $inventario--;

    // Usamos la función echo para imprimir un mensaje, concatenando texto con el valor actual de $inventario.
    // Esto mostrará: "Tengo 9 elementos en el inventario".
    echo "Tengo " . $inventario . " elementos en el inventario";
?>
